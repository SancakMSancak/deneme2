package com.MustafaSancak;

public class _11_NumbersSystem {
    public static void main(String[] args) {
        System.out.println("Sayı Sistemleri");

        // Binary 01
        int binary = 0b0101010;
        System.out.println(binary);

        // Octal: 01234567
        int octal = 0402;
        System.out.println(octal);

        // decimal: 0123456789
        int decimal = 777;
        System.out.println(decimal);

        // hexadecimal (onaltılık): 0123456789abcdef
        int hexa = 0x625ab;
        System.out.println(hexa);
    }
}
